package com.example.practice.condingtest;

public interface MvcPattern {

    /*

        Mvc 패턴은 3가지 요소의 앞 글자를 따와 만든 소프트웨어 디자인 패턴입니다.

        M, Model을 의미하며 데이터를 처리하고 비즈니스 로직을 수행하는 역할을 담당하고 있습니다.
        또한 , 모델은 Controller와 View에 대한 어떠한 정보도 알고 있으면 안됩니다.

        V, View를 의미합니다.
        뷰의 경우 model에서 처리한 데이터를 클라이언트에게 출력해주는 역할을 합니다.
        또한 , view의 경우 Controller와 Model에 대한 정보를 알고있으면 안됩니다.

        C, Controller를 의미합니다.
        컨트롤러의 경우 model과 view의 중재자 역할을 합니다.
        때문에 model , view에 대한 정보를 알고 있어야합니다.


     */

}
